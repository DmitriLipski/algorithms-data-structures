

function sortZero(arr) {
    const sorted = arr.reduce((acc, curr) => {
        const regular = curr === 0 ? [] : curr;
        const zero = curr === 0 ? curr : [];
        return [ [...acc[0].concat(regular)], [...acc[1].concat(zero)]]
        }, [[], []]);
    return sorted[0].concat(sorted[1])
}

