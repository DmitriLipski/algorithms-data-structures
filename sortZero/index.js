const { assert } = chai;

describe("sortZero", () => {
    it("gets correct answer", () => {
        assert.deepEqual(sortZero([1, true, 0, "text", false, 0]), [1, true, "text", false, 0, 0]);
        assert.deepEqual(sortZero([1, 0, 5, 10, 5]), [1, 5, 10, 5, 0]);
    });
});

