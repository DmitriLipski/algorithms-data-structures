mocha.setup("bdd");
const { assert } = chai;

describe("domainName", () => {
    it("works great", () => {
        assert.equal(domainName("http://google.com"), "google");
        assert.equal(domainName("http://google.co.jp"), "google");
        assert.equal(domainName("www.xakep.ru"), "xakep");
        assert.equal(domainName("https://youtube.com"), "youtube");
    });
});

mocha.run();
