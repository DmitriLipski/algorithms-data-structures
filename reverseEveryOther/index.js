

function reverse(arr) {
    return arr
        .trim()
        .split(" ")
        .map((word, index) => {
        return index % 2 === 1 ? word.split("").reverse().join("") : word;
    })
        .join(" ")
}

console.log("result =>", reverse("Reverse this string, please!"));

