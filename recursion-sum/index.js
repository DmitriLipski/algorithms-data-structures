mocha.setup("bdd");
const { assert } = chai;

describe("Vowels", () => {
  it("gets correct sum", () => {
    assert.equal(sum([1, 2, 3]), 6);
    assert.equal(sum([20, 5, 3, 2, 7, 3]), 40);
  });
  it("gets correct sum 2", () => {
    assert.equal(sum([10, 20, 30]), 60);
  });
  it("gets correct sum for empty array", () => {
    assert.equal(sum([]), 0);
  });
  it("gets correct sum for array with one item", () => {
    assert.equal(sum([7]), 7);
  });
});

mocha.run();
